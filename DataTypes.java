class DataTypes{
public static void main(String []arg){

char a = 'G';
int b = 32;
float f = 4.12345f;
short s = 56;
double d = 4.355353;
long l = 12121;

System.out.println("char: " + a);
System.out.println("integer: " + b);
System.out.println("short: " + s);
System.out.println("float: " + f);
System.out.println("double: " + d);
System.out.println("long: " + l);
}
};
